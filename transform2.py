import re

def shift(path_str, dx, dy):
    commands = re.findall(r'[A-Za-z]|[-+]?\d*\.\d+|[-+]?\d+', path_str)
    out = []
    i = 0
    while i < len(commands):
        tok = commands[i]
        if tok.isalpha():
            out.append(tok)
            # handle cases like z or Z
            i += 1
        else:
            x = float(commands[i])
            y = float(commands[i+1])
            out.append(f"{x+dx},{y+dy}")
            i += 2
            
    res = []
    for t in out:
        if t.isalpha(): res.append(t)
        else: res.append(t + " ")
    return "".join(res).replace(" Z", "Z").strip()

